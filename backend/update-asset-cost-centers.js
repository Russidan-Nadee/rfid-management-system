function parseAssetData(data) {
  const lines = data.trim().split('\n');
  const mapping = {};

  for (const line of lines) {
    const [assetNo, costCenter] = line.split('\t');
    if (assetNo) {
      mapping[assetNo] = costCenter && costCenter.trim() ? costCenter.trim() : null;
    }
  }

  return mapping;
}

async function updateAssetCostCenters() {
  console.log('🔄 Starting asset cost center update...');
  await prisma.$connect();

  try {
    const MAPPING = parseAssetData(ASSET_COST_CENTER_DATA);
    console.log(`📊 Found ${Object.keys(MAPPING).length} assets in mapping`);

    let updated = 0;
    let skipped = 0;

    // อัปเดตทีละตัว
    for (const [assetNo, costCenterCode] of Object.entries(MAPPING)) {
      try {
        const result = await prisma.asset_master.updateMany({
          where: { asset_no: assetNo },
          data: { cost_center_code: costCenterCode }
        });

        if (result.count > 0) {
          updated++;
          const status = costCenterCode ? `→ ${costCenterCode}` : '→ null';
          console.log(`✅ Updated ${assetNo} ${status}`);
        } else {
          skipped++;
          console.log(`⏭️  Skip ${assetNo} (not found in database)`);
        }
      } catch (error) {
        console.error(`❌ Failed to update ${assetNo}:`, error.message);
      }
    }

    console.log(`\n📊 Update Summary: ${updated} updated, ${skipped} skipped`);

    // ตรวจสอบผลลัพธ์
    const assetsWithCostCenter = await prisma.asset_master.count({
      where: { cost_center_code: { not: null } }
    });

    const totalAssets = await prisma.asset_master.count();

    console.log(`\n📈 Final Result:`);
    console.log(`   ✅ Assets with Cost Center: ${assetsWithCostCenter}/${totalAssets} (${((assetsWithCostCenter/totalAssets)*100).toFixed(1)}%)`);

  } catch (error) {
    console.error('❌ Error during update:', error.message);
    throw error;
  } finally {
    await prisma.$disconnect();
  }
}

